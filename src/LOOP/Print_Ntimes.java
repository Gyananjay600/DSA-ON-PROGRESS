package LOOP;

import java.util.Scanner;

public class Print_Ntimes {
    public static void main(String[] args) {
        Scanner Sc = new Scanner(System.in);
        System.out.println("Enter the number which you want to print:");
        int n = Sc.nextInt();
        for(int i = 1;i<=n;i++){
            System.out.println("Gyana");
        }
    }
}
