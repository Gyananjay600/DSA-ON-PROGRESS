package LOOP;
import java.util.Scanner;
public class EvenNumbers {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        // 100 iteration
        for(int i=1;i<=100;i+=1){
            if(i%2==0){
                System.out.println(i+" ");
            }
        }

    }
    
}




