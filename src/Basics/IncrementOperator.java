package Basics;

public class IncrementOperator {
    public static void main(String[] args) {
        int x = 10;
        x++; //post increment
        System.out.println(x);
        ++x; // pre increment
        System.out.println(x);
        x--; // post decrement
        System.out.println(x);
        --x; // pre decrement
        System.out.println(x);
    }
}
