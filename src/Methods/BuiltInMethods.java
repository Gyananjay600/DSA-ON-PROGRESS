package Methods;

public class BuiltInMethods {
    public static void main(String[] args) {
        System.out.println(Math.min(4, 8));
        System.out.println(Math.PI);
        System.out.println(Math.random());
        System.out.println(Math.max(5, 7));
        System.out.println(Math.abs(6.4));
        System.out.println(Math.sqrt(78));
        System.out.println(Math.floorDiv(12, 45));
        System.out.println();
    }
}
